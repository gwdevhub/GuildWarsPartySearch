/**
 *
 * @param map_id {number}
 * @returns {{continent: number, map_type: number, description_id: number, flags: number, campaign: number, region: number, name_id: number}|{continent: number, map_type: number, description_id: number, flags: number, campaign: number, region: number, name_id: number}|{continent: number, map_type: number, description_id: number, flags: number, campaign: number, region: number, name_id: number}|{continent: number, map_type: number, description_id: number, flags: number, campaign: number, region: number, name_id: number}|{continent: number, map_type: number, description_id: number, flags: number, campaign: number, region: number, name_id: number}|null}
 */
export function getMapInfo(map_id) {
  if(map_id >= map_info.length)
    return null;
  return map_info[map_id];
}

/**
 *
 * @param map_id
 * @return {boolean}
 */
export function isValidOutpost(map_id) {
  const map_info = getMapInfo(map_id);
  if(!map_info) return false;
  if ((map_info.flags & 0x5000000) === 0x5000000)
    return false; // e.g. "wrong" augury rock is map 119, no NPCs
  if ((map_info.flags & 0x80000000) === 0x80000000)
    return false; // e.g. Debug map
  switch (map_info.map_type) {
    case map_types.City:
    case map_types.CompetitiveMission:
    case map_types.CooperativeMission:
    case map_types.EliteMission:
    case map_types.MissionOutpost:
    case map_types.Outpost:
      return true;
  }
  return false;
}

/**
 *
 * @param map_id {number}
 * @returns {null|string}
 */
export function getMapName(map_id) {
  const info = getMapInfo(map_id);
  if(!info)
    return null;
  if(!info.name_id)
    return null;
  return encoded_strings[info.name_id+''] || null;
}

/**
 *
 * @param district_region {number}
 * @param district_language {number}
 * @returns {string}
 */
export function getDistrictName(district_region, district_language = -1) {
  if(district_region === district_regions.International && district_language !== -1)
    return district_region_info[district_region].name+' '+language_info[district_language].abbr;
  return district_region_info[district_region].name;
}
/**
 *
 * @param district_region {number}
 * @param district_language {number}
 * @returns {string}
 */
export function getDistrictAbbreviation(district_region, district_language) {
  if(district_region === district_regions.International)
    return language_info[district_language].abbr;
  return district_regions[district_language].abbr;
}

export function region_from_district(district) {
  switch(district) {
    case districts.International: return district_regions.International;
    case districts.American: return district_regions.America;
    case districts.EuropeEnglish:
    case districts.EuropeFrench:
    case districts.EuropeGerman:
    case districts.EuropeItalian:
    case districts.EuropeSpanish: return district_regions.Europe;
    case districts.AsiaKorean: return district_regions.Korea;
    case districts.AsiaChinese: return district_regions.China;
    case districts.AsiaJapanese: return district_regions.Japan;
  }
  return district_regions.America;
}

export function district_from_region(district_region, district_language) {
  switch(district_region) {
    case district_regions.International: return districts.International;
    case district_regions.America: return districts.American;
    case district_regions.Korea: return districts.AsiaKorean;
    case district_regions.China: return districts.AsiaChinese;
    case district_regions.Japan: return districts.AsiaJapanese;
    case district_regions.Europe:
      switch(district_language) {
        case languages.French: return districts.EuropeFrench;
        case languages.German: return districts.EuropeGerman;
        case languages.Spanish: return districts.EuropeSpanish;
        case languages.Italian: return districts.EuropeItalian;
        case languages.Polish: return districts.EuropePolish;
        case languages.Russian: return districts.EuropeRussian;
      }
      return districts.EuropeEnglish; // English
  }
  return districts.Current;
}
